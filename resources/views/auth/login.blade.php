    <script>
        function change(){
            $('.toggle-password').toggleClass("fa-eye fa-eye-slash");
            var input = $($('.toggle-password').attr("toggle"));
            if (input.attr("type") == "password") {
                input.attr("type", "text");
            } else {
                input.attr("type", "password");
            }
        }
